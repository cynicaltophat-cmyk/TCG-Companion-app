/**
 * Helper to add multiple cards to the global list with a shared set name
 */
const registerSet = (setName: string, cards: Omit<GundamCard, "set">[]) => {
  const cardsWithSet = cards.map(card => ({ ...card, set: setName }));
  GUNDAM_CARDS.push(...cardsWithSet);
};

