export type ArtVariantType = "Base art" | "Parallel" | "Beta" | "Beta Parallel" | "Premium" | "Championship";

export interface ArtVariant {
  type: ArtVariantType;
  imageUrl: string;
}

export interface GundamCard {
  id: string;
  name: string;
  japaneseName?: string;
  set: string;
  cardNumber: string;
  type: "Unit" | "Pilot" | "Command" | "Base";
  color: "Red" | "Blue" | "Green" | "White" | "Black" | "Yellow" | "Purple";
  rarity: "C" | "U" | "R" | "SR" | "UR" | "LR";
  cost: number;
  level?: number;
  ap?: number;
  hp?: number;
  ability: string;
  flavorText?: string;
  imageUrl: string;
  altImageUrl?: string; // Kept for backward compatibility
  variants?: ArtVariant[];
  traits?: string[];
  link?: string;
}

